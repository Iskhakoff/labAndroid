package com.example.machine_time.lab02;


public class User {

    String name;
    String surname;
    String lastname;
    String login;
    String rating;


    public User(String name, String surname, String lastname, String login, String rating) {
        this.name = name;
        this.surname = surname;
        this.lastname = lastname;
        this.login = login;
        this.rating = rating;
    }
}
